package amusementpark_demo.com.baldsunbirds.utils;

/**
 *
 * @author Arbab Ali
 */
public class Velocity {
    Integer x;
    Integer y;

    public Velocity(Integer x, Integer y) {
        this.x = x;
        this.y = y;
    }

    public Velocity() {
          this.x = 0;
        this.y = 0;
    }

    public Integer getX() {
        return x;
    }

    public Integer getY() {
        return y;
    }
    
    
}
